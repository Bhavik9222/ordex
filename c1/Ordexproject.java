package com.ordex.c1;

public class Ordexproject {
    // Access modifiers
    private String name;
    private int age;

    // Constructor
    public Ordexproject(String name, int age) {
        this.name = name;
        this.age = age;
    }

    // Getters
    public String getname() {
        return name;
    }

    public int getage() {
        return age;
    }
}
